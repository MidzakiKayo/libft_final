#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t b)
{
	size_t	i;

	i = 0;
	while (i < b)
	{
		if (s1[i] != s2[i] || !s1[i] || !s2[i])
			return ((unsigned char)s1[i] - (unsigned char)s2[i]);
		i++;
	}
	return (0);
}
